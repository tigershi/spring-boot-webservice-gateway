package cn.picc.finance.sample.model;

import java.io.Serializable;

public class TestStudent implements Serializable {

    private static final long serialVersionUID = 3751149603902649268L;

    private String id;
    private String name;
    private int age;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }


}
